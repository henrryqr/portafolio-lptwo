    glBegin(GL_POLYGON)
    glVertex2f(7, 9)
    glVertex2f(5, 7)
    glVertex2f(4, 6)
    glVertex2f(4, 5)
    glVertex2f(6, 3)
    glVertex2f(7, 4)
