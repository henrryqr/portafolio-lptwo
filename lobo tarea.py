    glVertex2f(3, -7)
    glVertex2f(1, -8)
    glEnd()
    
    glBegin(GL_LINE_LOOP)
    glVertex2f(-5, 0)
    glVertex2f(-4, 0)
    glVertex2f(-4, -1)
    glEnd()
    
    glColor3f(0.7, 0.7, 0.7)
    glBegin(GL_POLYGON)
    glVertex2f(-4, 0)
    glVertex2f(-4, -1)
