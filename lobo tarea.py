    glVertex2f(-9, -6)
    glVertex2f(-8, -7)
    glVertex2f(-7, -6)
    glVertex2f(-4, -9)
    glVertex2f(-1, -10)
    glVertex2f(0, -10)
    glEnd()
    
    glBegin(GL_LINE_STRIP)
    glVertex2f(0, 7)
    glVertex2f(2, 6)
    glVertex2f(3, 7)
    glVertex2f(5, 9)
